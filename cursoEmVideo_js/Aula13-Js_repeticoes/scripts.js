/*Repetições -- While = enquanto*/

/*Estrutura de repetição - Com teste lógico no início*/
console.log('----/While=enquanto/----');//projeto
var c = 1;
while (c <= 6) {
    console.log(`Passo ${c}`);
    c++;//contador c = c+1
}
console.log('----//----');//espaço

/*Estrutura de repetição - Com teste lógico no final*/
console.log('----/do=faça While=enquanto/----');//projeto
var r = 1;
do{
    console.log(`Passo ${r}`);
    r++;
}while (r <= 6)
console.log('----//----');//espaço

/*Estrutura de repetição - for*/
console.log('----/for=parar/----');//projeto
 //(inicialização;teste lógico;incrementação )
for(var c = 1; c <= 6; c++){
    console.log(`Passo ${c}`)
}
